use election;

## Total Seats
select distinct count(`Parliament Constituency`)  as Total_seats
from constituencywise_results;

# What is the total number of seats available for elections in each state
select s.state as state_name,count(cr.`Parliament Constituency`) as total_seats
from constituencywise_results cr 
inner join statewise_results sr 
on cr.`Parliament Constituency`=sr.`Parliament Constituency`
inner join states s 
on sr.`State ID`=s.`State ID`
group by 1;

